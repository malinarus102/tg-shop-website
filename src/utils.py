"""
Общие утилиты для расчёта браслета.
Единственное место где живёт формула — менять только здесь.
"""


def wrist_to_links(wrist_cm: float) -> int:
    """Количество звеньев по обхвату запястья.
    
    Формула: обхват + 1, минимум 1 звено.
    Примеры:
        17.0 см → 18 звеньев
        17.5 см → 18 звеньев  (int отбрасывает дробь)
        20.0 см → 21 звено
    """
    if wrist_cm <= 0:
        raise ValueError(f"Обхват должен быть больше 0, получено: {wrist_cm}")
    return int(wrist_cm) + 1


def validate_wrist_size(wrist_cm: float) -> str | None:
    """Проверяет корректность размера запястья.
    Возвращает текст ошибки или None если всё ок.
    """
    if wrist_cm <= 0:
        return "Обхват должен быть больше 0 см."
    if wrist_cm < 10:
        return "Слишком маленький обхват. Минимум 10 см."
    if wrist_cm > 40:
        return "Слишком большой обхват. Максимум 40 см."
    return None
